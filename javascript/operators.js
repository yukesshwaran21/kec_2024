//Asignment Operators
let x = 5;

//Arithmetic Operators
let y = 10;
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

//Relational Operators
console.log(x > y);
console.log(x < y);
console.log(x >= y);
console.log(x <= y);
console.log(x == y);
console.log(x != y);

//Logical Operators
console.log(x > 5 && y > 5);
console.log(x > 5 || y > 5);
